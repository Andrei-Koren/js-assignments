/**
 * Returns the string representation of rectangle with specified width and height
 * using pseudograhic chars
 *
 * @param {number} width
 * @param {number} height
 * @return {string}
 *
 * @example
 *
 *            '┌────┐\n'+
 *  (6,4) =>  '│    │\n'+
 *            '│    │\n'+
 *            '└────┘\n'
 *
 *  (2,2) =>  '┌┐\n'+
 *            '└┘\n'
 *
 *             '┌──────────┐\n'+
 *  (12,3) =>  '│          │\n'+
 *             '└──────────┘\n'
 *
 */
function getRectangleString(width, height) {
    let top_left = "┌";
    let top_right = "┐";
    let bottom_left = "└";
    let bottom_right = "┘";
    let vertical = "│";
    let horizontal = "─";
    let str = "";
    let end_l ="\n";
    for (let y = 1; y <= height; y++) {
        for (let x = 1; x <= width; x++) {
            if ((x===1) & (y===1)){
                str = str + top_left;
                continue;
                }
            if (((y === 1) || (y === height)) & (x!=1) & (x!=width)){
                str = str + horizontal;
                continue;
                }
            if ((y===1) & (x===width)){
                str = str + top_right;
                continue;
                }
            if ((y!=1) & (y!=height) & ((x===1) || (x===width))){
                str = str + vertical;
                continue;
                }
            if ((y===height) & (x===1)){
                str = str + bottom_left;
                continue;
                }
            if ((y===height) & (x===width)){
                str = str + bottom_right;
                break;
                }
            str = str + " ";
     }
    str = str + end_l;
    }
return str;
}


